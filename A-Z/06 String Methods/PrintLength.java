/*
 * System.out.println(hello.length());    Here "(hello.length())"" calculates the length of the String given by length by hello and gives it as output.
 */

public class PrintLength {
    public static void main(String[]args) {

        String hello = "Hello , How are you...!" ;
        System.out.println(hello.length()); 

    }
}

// Output : 23
